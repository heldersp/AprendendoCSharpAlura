using System;
using System.Collections.Generic;
using System.Linq;
using System.Text;
using System.Threading.Tasks;

namespace DesafioWhileParaFor
{
    class Program
    {
        static void Main(string[] args)
        {
            Console.WriteLine("Executando desafio while para for");

            for (int contador = 1; contador <= 10; contador++)
            {
                Console.WriteLine(contador);
            }


            Console.ReadLine();

        }
    }
}
